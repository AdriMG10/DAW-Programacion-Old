public class PruebaMesSwitch {
    public static void main(String[] args) {
        
        String enero = "Estamos en enero";
        String febrero = "Estamos en febrero";
        String marzo = "Estamos en marzo";
        String abril = "Estamos en abril";
        String mayo = "Estamos en mayo";
        String junio = "Estamos en junio";
        String julio = "Estamos en julio";
        String agosto = "Estamos en agosto";
        String septiembre = "Estamos en septiembre";
        String octubre = "Estamos en octubre";
        String noviembre = "Estamos en noviembre";
        String diciembre = "Estamos en diciembre";

        int mes = 11;

        switch (mes) {

            case 1: System.out.println(enero);
                break;

            case 2: System.out.println(febrero);
                break;

            case 3: System.out.println(marzo);
                break;

            case 4: System.out.println(abril);
                break;

            case 5: System.out.println(mayo);
                break;

            case 6: System.out.println(junio);
                break;

            case 7: System.out.println(julio);
                break;

            case 8: System.out.println(agosto);
                break;

            case 9: System.out.println(septiembre);
                break;

            case 10: System.out.println(octubre);
                break;

            case 11: System.out.println(noviembre);
                break;

            case 12: System.out.println(diciembre);
                break;       
        }
    }
}
